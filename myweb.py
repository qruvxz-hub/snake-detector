import streamlit as st

# 1. เขียนหัวข้อหน้าเว็บ
st.title("เว็บเปิดกล้องของฉัน")

# 2. สร้างช่องเปิดกล้อง (บรรทัดนี้คือพระเอก!)
picture = st.camera_input("กดปุ่มเพื่อถ่ายรูป")

# 3. ถ้าถ่ายรูปแล้ว ให้ทำอะไรต่อ?
if picture:
    st.write("ถ่ายเสร็จแล้ว! นี่คือรูปของคุณ:")
    st.image(picture) # โชว์รูปบนหน้าเว็บ
